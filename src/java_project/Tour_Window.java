    public void Show_Product_In_JTable()
    {
            
        ArrayList<Tour> list = getTourList();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
         
        Object[] row = new Object[7];
        for(int i=0; i<list.size();i++)
        {
            row[0]=list.get(i).getTid();
            row[1]=list.get(i).getTname();
            row[2]=list.get(i).getUname();
            row[3]=list.get(i).getPN();
            row[4]=list.get(i).getcost();
            row[5]=list.get(i).getvehicle();
            row[6]=list.get(i).gethotel();
            

            model.addRow(row);
        }
    }
