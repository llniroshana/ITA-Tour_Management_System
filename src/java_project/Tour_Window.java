    public ArrayList<Tour> getTourList(){
         
     ArrayList<Tour>  tourList = new ArrayList<Tour>();
    Connection con = getConnection();
     
     String query = "SELECT t.Tour_ID,t.Tour_Place,u.User_Name,u.Phone_Number,t.Cost,t.V_Name,t.H_Name FROM Tour t, User u WHERE t.User_Name=u.User_Name";
     
     Statement st;
     ResultSet rs;
     
     try{
        st = con.createStatement();
        rs = st.executeQuery(query);
        Tour tour;
     
            while(rs.next())
            {
                tour = new Tour(rs.getInt("Tour_ID"),rs.getString("Tour_Place"), rs.getString("User_Name"),rs.getString("Phone_Number"), rs.getString("Cost"),rs.getString("V_Name"),rs.getString("H_Name"));
                tourList.add(tour);
            }
     
    }catch(SQLException ex){
        Logger.getLogger(Main_Window.class.getName()).log(Level.SEVERE, null, ex);
        }
    return tourList;
}
